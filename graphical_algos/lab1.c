void do_lab1(int argc , char *argv[]){


    XPM *img=NULL;
    unsigned int i,j;
    char s[2];

    img=initXPM(50 , 50 , 1 , 50);

    s[1]='\0';
    for(i=0 ; i < 50 ; i++){
        s[0]='A'+i;
        setXPMColor(img, i, r[i] , 0x00 , 0x00 , s);
    }

    for(i=0 ; i < 50 ; i++){
        for(j=0 ; j < 50 ; j++){
            putXPMpixel(img , i , j , i);
        }
    }
    writeXPMtofile(img , "lab1out.xpm");
    freeXPM(&img);
}

